public class Caracteristica {

		private Long cdCarac;
		private Long cdValCarac;
		private String descricaoValor;
		public Long getCdCarac() {
			return cdCarac;
		}
		public void setCdCarac(Long cdCarac) {
			this.cdCarac = cdCarac;
		}
		public Long getCdValCarac() {
			return cdValCarac;
		}
		public void setCdValCarac(Long cdValCarac) {
			this.cdValCarac = cdValCarac;
		}
		public String getDescricaoValor() {
			return descricaoValor;
		}
		public void setDescricaoValor(String descricaoValor) {
			this.descricaoValor = descricaoValor;
		}
		
		
}
